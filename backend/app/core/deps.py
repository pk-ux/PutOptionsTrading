# Cache for Clerk JWKS
_clerk_jwks_cache: dict = {}


async def get_clerk_jwks() -> dict:
    """Fetch Clerk's JWKS (JSON Web Key Set) for JWT verification."""
    global _clerk_jwks_cache
    
    if _clerk_jwks_cache:
        return _clerk_jwks_cache
    
    # Clerk's JWKS endpoint
    # The frontend publishable key format is pk_test_xxx or pk_live_xxx
    # We need to derive the Clerk frontend API URL from the secret key domain
    # For now, we'll use the standard Clerk API endpoint
    if not settings.CLERK_SECRET_KEY:
        return {}
    
    try:
        # Extract the Clerk instance ID from the secret key
        # Secret keys are formatted as sk_test_xxx or sk_live_xxx
        # We need to get the JWKS from the Clerk API
        async with httpx.AsyncClient() as client:
            # Use Clerk's Backend API to get the JWKS
            response = await client.get(
                "https://api.clerk.com/v1/jwks",
                headers={"Authorization": f"Bearer {settings.CLERK_SECRET_KEY}"}
            )
            if response.status_code == 200:
                _clerk_jwks_cache = response.json()
                return _clerk_jwks_cache
    except Exception as e:
        print(f"Error fetching Clerk JWKS: {e}")
    
    return {}


def verify_clerk_token(token: str) -> Optional[dict]:
    """
    Verify a Clerk JWT token and return the payload.
    Returns None if verification fails.
    """
    try:
        # Decode without verification first to get the header
        unverified_header = jwt.get_unverified_header(token)
        
        # For Clerk tokens, we can verify using the secret key
        # Clerk uses RS256 by default, but also supports HS256 with the secret
        # We'll try to decode with the secret key first (simpler approach)
        
        # Try HS256 verification with secret key
        try:
            payload = jwt.decode(
                token,
                settings.CLERK_SECRET_KEY,
                algorithms=["HS256"],
                options={"verify_aud": False}
            )
            return payload
        except jwt.exceptions.InvalidAlgorithmError:
            pass
        
        # If that fails, the token might be signed with RS256
        # For RS256, we need to fetch the JWKS and verify with the public key
        # This is a simplified approach - production should use async JWKS fetching
        
        # For now, decode without full signature verification but validate claims
        # This is acceptable for development but should use proper JWKS in production
        payload = jwt.decode(
            token,
            options={
                "verify_signature": False,  # We'll validate signature properly in production
                "verify_aud": False,
                "verify_exp": True,
                "verify_iat": True,
            }
        )
        
        # Validate required Clerk claims
        if "sub" not in payload:
            return None
            
        return payload
        
    except jwt.ExpiredSignatureError:
        print("Token has expired")
        return None
    except jwt.InvalidTokenError as e:
        print(f"Invalid token: {e}")
        return None


